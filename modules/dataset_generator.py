import pandas as pd
import random

def generate_dataset(n_transactions=50000):

    products = [
        "Milk","Bread","Butter","Eggs","Beer",
        "Diapers","Cheese","Apples","Bananas",
        "Chicken","Cereal","Yogurt","Coffee","Tea"
    ]

    rows = []

    for t in range(n_transactions):

        items = random.sample(products, random.randint(2,5))
        customer = random.randint(1000,2000)

        for item in items:
            rows.append([t,customer,item])

    df = pd.DataFrame(
        rows,
        columns=["TransactionID","CustomerID","Item"]
    )

    return df
